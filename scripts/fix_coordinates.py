ROAD_NAMES = [
    "ถนนพหลโยธิน", "ถนนสุขุมวิท", "ถนนวิภาวดีรังสิต",
    "ถนนรัชดาภิเษก", "ถนนลาดพร้าว", "ถนนเพชรบุรี",
    "ถนนบางนา–ตราด", "ถนนกาญจนาภิเษก", "ทางหลวงหมายเลข 1",
    "ทางหลวงหมายเลข 2", "ทางหลวงหมายเลข 4",
]


async def fix():
    conn = await asyncpg.connect(DB_DSN)

    rows = await conn.fetch("SELECT id FROM reports WHERE source = 'seed'")
    print(f"Updating {len(rows)} seed records with inland coordinates...")

    async with conn.transaction():
        for row in rows:
            province, base_lat, base_lng = random.choice(INLAND_POINTS)
            lat  = round(base_lat + random.uniform(-0.02, 0.02), 7)
            lng  = round(base_lng + random.uniform(-0.02, 0.02), 7)
            road = random.choice(ROAD_NAMES)

            await conn.execute("""
                UPDATE reports
                SET lat = $1, lng = $2, province = $3, road_name = $4
                WHERE id = $5
            """, lat, lng, province, road, row["id"])

    await conn.close()
    print("Done — all coordinates updated to inland locations.")


asyncio.run(fix())
